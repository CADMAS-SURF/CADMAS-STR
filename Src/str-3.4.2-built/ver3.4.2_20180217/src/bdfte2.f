      SUBROUTINE BDFTE2(FC,XYZ,GRAV,R0,IGR,ITO)
C
      IMPLICIT REAL*8(A-H,O-Z)
      DIMENSION WGT(5),RL(4,5),XYZ(3,10),P(3,10),XJI(3,3),H(10)
     &         ,FC(3,10),VEC(3),GRAV(3),R0(3)
      DATA WGT / -0.133333333333333D0, 4*0.075D0 /
      DATA RL  / 4*0.25D0
     &  , 0.500000000000000D0, 0.166666666666667D0, 0.166666666666667D0
     &  , 0.166666666666667D0
     &  , 0.166666666666667D0, 0.500000000000000D0, 0.166666666666667D0
     &  , 0.166666666666667D0
     &  , 0.166666666666667D0, 0.166666666666667D0, 0.500000000000000D0
     &  , 0.166666666666667D0
     &  , 0.166666666666667D0, 0.166666666666667D0, 0.166666666666667D0
     &  , 0.500000000000000D0 /
C----&------------------------------------------------------------------
      ND=10
C
      DO 100 IG=1,5
C
        CALL DERTE2(RL(1,IG),P)
        CALL JACOB3(ND,P,XYZ,XJI,DET,ITO)
C
        CALL SFNTE2(RL(1,IG),H)
C
        DO 110 I=1,ND
C
          IF(IGR .EQ. 1) THEN
            CALL SHIFT1(VEC,GRAV,3)
          ELSE
            CALL SUBVEC(VEC,XYZ(1,I),R0,3)
          ENDIF
C
          DO 120 J=1,3
            FC(J,I)=FC(J,I)+H(I)*VEC(J)*DET*WGT(IG)
  120     CONTINUE
C
  110   CONTINUE
C
  100 CONTINUE
C
      RETURN
      END
